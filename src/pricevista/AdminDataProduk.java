    private void load_table(){
        // membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel(new Object[][]{}, new String[] {
        "ID PRODUCT", "NAMA PRODUCT", "JENIS PRODUCT", "HARGA SATUAN", "HARGA PERPACK"
    }) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false; // Mencegah pengeditan semua kolom
        }
    };
        
        //menampilkan data database kedalam tabel
        try {
            int no=1;
            String sql = "select * from products";
            java.sql.Connection conn=(Connection)koneksi.config.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{
                    res.getString(1),
                    res.getString(2),
                    res.getString(3),
                    res.getString(4),
                    res.getString(5)
                });
            }
            jTable1.setModel(model);
        } catch (Exception e) {
        }
        
        // Tambahkan MouseListener untuk menangkap data baris yang diklik
        jTable1.addMouseListener(new java.awt.event.MouseAdapter() {
        public void mouseClicked(java.awt.event.MouseEvent evt) {
            int selectedRow = jTable1.getSelectedRow();
            if (selectedRow != -1) {
                // Ambil data dari baris yang dipilih dan masukkan ke JTextFields
                txtidproduct.setText(jTable1.getValueAt(selectedRow, 0).toString());
                txtnamaproduct.setText(jTable1.getValueAt(selectedRow, 1).toString());
                txtjenisproduct.setText(jTable1.getValueAt(selectedRow, 2).toString());
                txthargasatuan.setText(jTable1.getValueAt(selectedRow, 3).toString());
                txthargaperpack.setText(jTable1.getValueAt(selectedRow, 4).toString());
            }
        }
    });
    }

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
        try {
            // Query untuk mengupdate data berdasarkan id_mobil
            String sql = "UPDATE products SET nama_produk=?, jenis_produk=?, harga_satuan=?, harga_perpack=? WHERE id_produk=?";

            // Koneksi ke database dan siapkan query
            java.sql.Connection conn = (Connection)koneksi.config.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);

            // Set parameter berdasarkan data dari text field
            pst.setString(1, txtnamaproduct.getText());
            pst.setString(2, txtjenisproduct.getText());
            pst.setString(3, txthargasatuan.getText());
            pst.setString(4, txthargaperpack.getText());
            pst.setString(5, txtidproduct.getText()); // Acuan utama adalah id_mobil

            // Eksekusi perintah update dan periksa jumlah baris yang diupdate
            int rowsAffected = pst.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil diperbarui");

                // Refresh tabel dan kosongkan form setelah update
                load_table();
                kosongkanForm();
            } else {
                // Jika tidak ada baris yang diupdate, berarti id_produk tidak ditemukan
                JOptionPane.showMessageDialog(this, "ID tidak ditemukan");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

